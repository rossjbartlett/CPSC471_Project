    {
       
        $u = Auth()->User();
        if (($u->SIN == $timesheet->SIN) || $u->isManager()) { // only show your own 
            $shifts = $timesheet->shifts();
            return view('timesheets.show', compact('timesheet', 'shifts'));
        } else {
            return redirect('timesheets');
        }

